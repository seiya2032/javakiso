package kadai1;

public class Variable01 {
    public static void main(String[] args){
        //加算
        int num1 = 1758071224;
        int num2 = 389412423;
        int sum = num1 + num2;
        System.out.println("num1 + num2 の計算結果は " + sum + "です。");

        //減算
        int num3 = 1625529294;
        int num4 = 521954353;
        int difference = num3 - num4;
        System.out.println("num3 - num4　の計算結果は　" + difference + "です。");

        //乗算
        int num5 = 8455;
        int num6 = 3941;
        int product = num5 * num6;
        System.out.println("num5 * num6 の計算結果は " + product + "です。");

        //除算
        int num7 = 64514887;
        int num8 = 9181;
        int quotient = num7 / num8;
        System.out.println("num7 / num8 の計算結果は " + quotient + "です。");

        //剰余
        int num9 = 1226995849;
        int num10 = 16807;
        int remainder = num9 % num10;
        System.out.println("num9 % num10 で割った余りは " + remainder + "です。");
    }
}
