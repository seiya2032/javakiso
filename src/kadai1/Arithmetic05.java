package kadai1;

public class Arithmetic05 {
    public static void main(String[]args){
        //加算
        int sum = 1768071224 + 389412423;
        System.out.println("1768071224 + 389412423 の計算結果は" + sum + "です。");

        //減算
        int difference = 1625529294 - 521954353;
        System.out.println("1625529294 - 521954353の計算結果は" + difference);

        //乗算
        int product = 8455 * 3941;
        System.out.println("8455 × 3941　の計算結果は " + product + "です。");


        //除算
        int quotient = 64514887 / 9181;
        System.out.println("64514887 ÷ 9181 の計算結果は　" + quotient + "です。");

        //剰余
        int remainder = 1226995849 % 16807;
        System.out.println("1226995849 を 16807 で割った余りは " +remainder + "です。");
    }
}
